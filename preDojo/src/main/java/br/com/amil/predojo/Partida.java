package br.com.amil.predojo;

public class Partida {
	
	private String idPartida;
	private String dtHrPartida;
	private String usuario;
	private String arma;
	
	/**
	 * @return the idPartida
	 */
	public String getIdPartida() {
		return idPartida;
	}
	/**
	 * @param idPartida the idPartida to set
	 */
	public void setIdPartida(String idPartida) {
		this.idPartida = idPartida;
	}
	/**
	 * @return the dtHrPartida
	 */
	public String getDtHrPartida() {
		return dtHrPartida;
	}
	/**
	 * @param dtHrPartida the dtHrPartida to set
	 */
	public void setDtHrPartida(String dtHrPartida) {
		this.dtHrPartida = dtHrPartida;
	}
	/**
	 * @return the usuario
	 */
	public String getUsuario() {
		return usuario;
	}
	/**
	 * @param usuario the usuario to set
	 */
	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}
	/**
	 * @return the arma
	 */
	public String getArma() {
		return arma;
	}
	/**
	 * @param arma the arma to set
	 */
	public void setArma(String arma) {
		this.arma = arma;
	}
}
